package Day4_100daysOfCode;

//Topic :Increment and Decrement
//Increment operator(++) used to increase the value of the given variable by one.
// Decrement operator(--) used to Decrease the value of the given variable by one.

public class IncrementDecrementConcept {
    public static void main(String[] args){

        int a=5;
        System.out.println(a);   //5
        a++; //6
        ++a; //7
        System.out.println(a); //7

    }
}
