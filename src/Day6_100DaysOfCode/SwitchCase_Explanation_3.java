package Day6_100DaysOfCode;

//Topic:: Switch Case
public class SwitchCase_Explanation_3 {
    public static void main(String[] args){

        int number=100;

        switch(number){
            case 10: System.out.println("First Case");
            case 20: System.out.println("Second Case");
            case 100: System.out.println("Third Case"); // This is MATCHING,So third case will be printed

        }
    }
}


