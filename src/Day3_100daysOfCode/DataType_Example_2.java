package Day3_100daysOfCode;

//Topic2 :Data Types
//Number Data: byte,short,int,long
//Character Data Type: To Represent characters
//boolean:To represent Logical values(true or false)
//Float data type:To Represent real numbers(float double)

public class DataType_Example_2 {
public static void main(String[] args){

    //Number Data

    int b=10;
    byte result=20;
    short b1=2;
    long l=96766355353L;

    //character data
    char c='c';

    //boolean
    boolean ex=true;
    boolean b2=false;

    //float data

    float f=2.0f;
    double d=3.02d;

    System.out.println(b);
    System.out.println(result);
    System.out.println(b1);
    System.out.println(l);
    System.out.println(c);
    System.out.println(ex);
    System.out.println(b2);
    System.out.println(f);
    System.out.println(d);




}
}
