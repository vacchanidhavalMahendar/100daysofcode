public class ConditionalIfElse_6 {
    public static void main(String[] args){

        int a=10;
        int b=2;

        if(a>b){

            System.out.println("Subtraction");
            System.out.println(a-b);
        }

        else if(a==b){
            System.out.println("Addition");
            System.out.println(a+b);

        }

        else if (a<b){
            System.out.println("a is lesser");
        }

    }

}

