package Day5_100daysOfCode;

public class ConditionalIfElse_5{
    public static void main(String[] args){


        int a=10;
        int b=2;

        if(a>b){

            System.out.println("Subtraction");
            System.out.println(a-b);
        }

        else{
            System.out.println("Addition");
            System.out.println(a+b);

        }

    }

}
