package Day8_100daysOfCode;

public class Loops_1 {
    public static void main(String[] args){

        //Syntax of for loop: for(initialization;condition;update){}
        //Start-->Initilization-->Condition--->Body--->Update

        int n=20;
        for(int i=0; i<n;++i)
        {
            System.out.println("*");
        }


    }

}