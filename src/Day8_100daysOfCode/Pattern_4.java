package Day8_100daysOfCode;

public class Pattern_4 {
    public static void main(String[] args){

        int n=20;
        for(int i=0;i<n;i++)
        {
            for(int j=0;j<n;j++)
            {
                System.out.print("*");
            }
            System.out.println();
        }

    }

}
